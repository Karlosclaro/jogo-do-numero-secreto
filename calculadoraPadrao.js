// Função para somar dois números
function soma(a, b) {
  return a + b;
}

// Função para subtrair dois números
function subtracao(a, b) {
  return a - b;
}

// Função para multiplicar dois números
function multiplicacao(a, b) {
  return a * b;
}

// Função para dividir dois números
function divisao(a, b) {
  return a / b;
}

// Função para calcular a raiz quadrada de um número
function raizQuadrada(a) {
  return Math.sqrt(a);
}

// Função para calcular a potência de um número
function potencia(a, b) {
  return Math.pow(a, b);
}

// Função para calcular o logaritmo de um número
function logaritmo(a) {
  return Math.log(a);
}

// Função para calcular o seno de um número
function seno(a) {
  return Math.sin(a);
}

// Função para calcular o cosseno de um número
function cosseno(a) {
  return Math.cos(a);
}

// Função para calcular a tangente de um número
function tangente(a) {
  return Math.tan(a);
}

// Função para calcular o arco seno de um número
function arcoSeno(a) {
  return Math.asin(a);
}

// Função para calcular o arco cosseno de um número
function arcoCosseno(a) {
  return Math.acos(a);
}

// Função para calcular o arco tangente de um número
function arcoTangente(a) {
  return Math.atan(a);
}

// Função para calcular o arco tangente de dois números
function arcoTangente2(a, b) {
  return Math.atan2(a, b);
}

// Função para calcular a hipotenusa de um triângulo retângulo
function hipotenusa(a, b) {
  return Math.sqrt(a * a + b * b);
}

